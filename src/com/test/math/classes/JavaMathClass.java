package com.test.math.classes;

public class JavaMathClass {

	public static void main(String[] args) {

         int a = 2;
         int b = -4;
         double c = 4.5;
         double d= -5.6;
         System.out.println(" abs: a="+ Math.abs(a)+" b="+ Math.abs(b)+" c="+ Math.abs(c)+" d="+ Math.abs(d));
         System.out.println(" ceil: a="+ Math.ceil(a)+" b="+ Math.ceil(b)+" c="+ Math.ceil(c)+" d="+ Math.ceil(d));
         System.out.println(" floor: a="+ Math.floor(a)+" b="+ Math.floor(b)+" c="+ Math.floor(c)+" d="+ Math.floor(d));
	}

}
